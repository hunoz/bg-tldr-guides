#!/usr/bin/env node

/**
 * Scans games/{name}/config.ts and generates games/registry.ts with static imports.
 * Run manually, as a pre-build step, or via the Metro file watcher.
 */

const fs = require('fs');
const path = require('path');

const GAMES_DIR = path.resolve(__dirname, '..', 'games');
const OUTPUT = path.join(GAMES_DIR, 'registry.ts');
const HEADER = '// Auto-generated by scripts/generate-registry.js — do not edit manually\n';

function generate() {
    const dirs = fs
        .readdirSync(GAMES_DIR, { withFileTypes: true })
        .filter(d => d.isDirectory() && fs.existsSync(path.join(GAMES_DIR, d.name, 'config.ts')))
        .map(d => d.name)
        .sort();

    const imports = dirs
        .map(dir => `import { config as ${toCamel(dir)}Config } from './${dir}/config';`)
        .join('\n');

    const entries = dirs.map(dir => `  ${toCamel(dir)}Config,`).join('\n');

    const content = `${HEADER}
import type { GameConfig } from '@/types/GameConfig';

${imports}

export const games: GameConfig[] = [
${entries}
];

export const gameIds: string[] = games.map((g) => g.id);

export function getGameById(id: string): GameConfig | undefined {
  return games.find((g) => g.id === id);
}
`;

    const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, 'utf8') : '';
    if (existing === content) return false; // no change
    fs.writeFileSync(OUTPUT, content, 'utf8');
    return true;
}

/** Convert kebab-case to camelCase: 'castles-of-burgundy' → 'castlesOfBurgundy' */
function toCamel(str) {
    return str.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

// Run immediately when called directly
if (require.main === module) {
    const changed = generate();
    if (changed) {
        console.log('[generate-registry] Updated games/registry.ts');
    } else {
        console.log('[generate-registry] games/registry.ts is up to date');
    }
}

module.exports = { generate };
