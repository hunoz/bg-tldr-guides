#!/usr/bin/env node

/**
 * Scans games/{name}/theme.ts and generates theme/themeMap.ts with static imports.
 * Run manually, as a pre-build step, or via the Metro file watcher.
 */

const fs = require('fs');
const path = require('path');

const GAMES_DIR = path.resolve(__dirname, '..', 'games');
const OUTPUT = path.resolve(__dirname, '..', 'theme', 'themeMap.ts');
const HEADER = '// Auto-generated by scripts/generate-theme-map.js — do not edit manually\n';

function generate() {
    const dirs = fs
        .readdirSync(GAMES_DIR, { withFileTypes: true })
        .filter(d => d.isDirectory() && fs.existsSync(path.join(GAMES_DIR, d.name, 'theme.ts')))
        .map(d => d.name)
        .sort();

    const imports = dirs
        .map(dir => `import { theme as ${toCamel(dir)}Theme } from '../games/${dir}/theme';`)
        .join('\n');

    const entries = dirs.map(dir => `  '${dir}': ${toCamel(dir)}Theme,`).join('\n');

    const content = `${HEADER}
import type { Theme } from './themes';
import { homeTheme } from './themes';

${imports}

export const themeMap: Record<string, Theme> = {
  '': homeTheme,
${entries}
};
`;

    const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, 'utf8') : '';
    if (existing === content) return false;
    fs.writeFileSync(OUTPUT, content, 'utf8');
    return true;
}

function toCamel(str) {
    return str.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

if (require.main === module) {
    const changed = generate();
    if (changed) {
        console.log('[generate-theme-map] Updated theme/themeMap.ts');
    } else {
        console.log('[generate-theme-map] theme/themeMap.ts is up to date');
    }
}

module.exports = { generate };
